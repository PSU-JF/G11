                <p>Su objetivo es poder tener la trazabilidad de la carga contenerizada desde el lugar de origen hasta su entrega en puerto o viceversa. Realizándolo a través de dispositivos denominados candados satelitales, los que además de reportar ubicación y ruta también tiene alertas por aperturas no autorizada de la puerta del contenedor o el intento de sabotaje del mismo dispositivo. El monitoreo de este servicio puede hacerse a través de la plataforma de seguimiento satelital de Sevicol Ltda.</p>

