                <h1>Seguridad Movil </h1>
                <hr>
                <p>Servicio que se presta a través de diferentes medios de transporte motorizados y no motorizados de acuerdo a la extensión y geografía del lugar, con personal calificado, entrenado y certificados con el fin de custodiar y proteger instalaciones con grandes áreas de extensión (internas/externas).

Esta modalidad aplica también si solo se requiere supervisión en sus instalaciones durante horas o días específicos. Para esto ofrecemos el servicio de vigilancia móvil con el cual revisamos, de acuerdo a la frecuencia contratada, el estado de sus instalaciones a través de los Supervisores Sevicol. Este servicio involucra los siguientes aspectos:</p>
<br>
<p class="texto">
✪ Disponibilidad las 24 horas del día, siete días a la semana.
✪ Reacción de los supervisores de la zona a las alarmas y eventualidades de cada instalación.
✪ Sectorización de las zonas asignadas para una pronta reacción.
✪ Personal uniformado y claramente identificado para mayor seguridad del cliente.
✪ Comunicación con diversos medios tecnológicos digitales o cualquier otro sistema dependiendo de la zona por proteger.</p>

