                ✪ Supervisión permanente de la operación a través de visitas a los puntos de servicio de acuerdo al nivel de riesgo y distribuidas por zonas para una rápida reacción.
                ✪ Diversos Sistema de control de rondas.
                ✪ Estructura nacional de apoyo al servicio.(Directores operativos, Coordinadores regionales y de zonas, y supervisores) quienes dirigirán y monitorearan la reacción y apoyo logístico ante cualquier eventualidad.
