Los escoltas Sevicol son seleccionados y entrenados con el propósito de desarrollar técnicas y tácticas orientadas a la protección de personas y mercancías que podrían encontrarse en inminente riesgo, mediante esquemas de protección, proporcionando todos los recursos físicos y tecnológicos necesarios para garantizar su tranquilidad.

